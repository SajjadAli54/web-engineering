console.log('parseInt');
console.log(parseInt('123'));   // 123
console.log(parseInt('123.123')); // 123
console.log(parseInt('123abcd'));   // 123
console.log(parseInt('qwes123'));   // NaN

console.log('parseFloat');
console.log(parseFloat('123'));   // 123
console.log(parseFloat('123.123')); // 123.123
console.log(parseFloat('123abcd'));   // 123
console.log(parseFloat('qwes123'));   // NaN

console.log('Number');
console.log(Number('123'));   // 123
console.log(Number('123.123')); // 123.123
console.log(Number('123abcd'));   // NaN
console.log(Number('qwes123'));   // NaN

